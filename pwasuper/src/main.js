import { createApp } from 'vue'
import App from './App.vue'
import router from './router'

// Importar estilos (Tailwind CSS y personalizados)
import './style.css'

// Importar Leaflet CSS globalmente
import 'leaflet/dist/leaflet.css'

// Corregir el problema del √≠cono de Leaflet
import { Icon } from 'leaflet'
delete Icon.Default.prototype._getIconUrl
Icon.Default.mergeOptions({
  iconRetinaUrl: '/leaflet/marker-icon-2x.png',
  iconUrl: '/leaflet/marker-icon.png',
  shadowUrl: '/leaflet/marker-shadow.png',
})

// Importar utilidad de registro del Service Worker
import { registerServiceWorker, waitForServiceWorkerReady } from './utils/serviceWorkerRegistration.js'

// Importar composable de notificaciones para inicializaci√≥n global
import { useNotifications } from './composables/useNotifications.js'

// Importar servicio de verificaci√≥n de mantenimiento
import { maintenanceCheckService } from './services/maintenanceCheckService.js'

// Inicializar estado de mantenimiento al cargar la aplicaci√≥n
const initMaintenanceState = () => {
  try {
    const initialState = maintenanceCheckService.getInitialMaintenanceState()
    console.log('üîß Estado inicial de mantenimiento inicializado:', initialState)
    
    // Asegurar que el estado global est√© configurado
    window.maintenanceMode = {
      enabled: initialState.enabled,
      message: initialState.message || 'La aplicaci√≥n est√° en mantenimiento'
    }
    
    return initialState
  } catch (error) {
    console.error('‚ùå Error inicializando estado de mantenimiento:', error)
    window.maintenanceMode = { enabled: false, message: '' }
    return { enabled: false, message: '' }
  }
}

// Inicializar estado de mantenimiento antes de crear la aplicaci√≥n
const maintenanceState = initMaintenanceState()

// Registrar el Service Worker para funcionalidad PWA offline
window.addEventListener('load', async () => {
  try {
    // Registrar el service worker
    const registration = await registerServiceWorker();
    
    // Esperar a que el service worker est√© listo
    await waitForServiceWorkerReady();
    
    console.log('‚úÖ Aplicaci√≥n lista con soporte offline');
    
    // Inicializar sistema de notificaciones global con sonido
    const { initializeGlobalAudio, requestNotificationPermission } = useNotifications();
    
    // Solicitar permisos de notificaci√≥n al usuario
    await requestNotificationPermission();
    
    // Inicializar audio global (despu√©s de interacci√≥n del usuario)
    document.addEventListener('click', () => {
      initializeGlobalAudio();
    }, { once: true });
    
    console.log('üîî Sistema de notificaciones con sonido inicializado');
    
  } catch (error) {
    console.error('‚ùå Error al inicializar la aplicaci√≥n:', error);
  }
});

// Crear aplicaci√≥n
const app = createApp(App)

// Usar router
app.use(router)

// Montar aplicaci√≥n
app.mount('#app')
